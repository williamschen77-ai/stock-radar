// 搜尋：代號或中文名稱，模糊比對
export function searchStock(query) {
  const q = query.trim();
  if (!q) return [];
  const ql = q.toLowerCase();
  const exact = STOCK_MAP[q];
  if (exact) return [{ code: q, ...exact }];
  return Object.entries(STOCK_MAP)
    .filter(([code, v]) =>
      code.startsWith(ql) ||
      v.name.includes(q) ||
      v.sector.includes(q) ||
      code.includes(ql)
    )
    .slice(0, 8)
    .map(([code, v]) => ({ code, ...v }));
}

export function getStockName(code) {
  return STOCK_MAP[code]?.name || null;
}

export function getStockInfo(code) {
  return STOCK_MAP[code] || null;
}
